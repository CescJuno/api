package com.skt.api.bible.vo;

public class BibleBook {
	String bibleType;
	String bibleTitle;
	String bibleSubTitle;
	public String getBibleType() {
		return bibleType;
	}
	public void setBibleType(String bibleType) {
		this.bibleType = bibleType;
	}
	public String getBibleTitle() {
		return bibleTitle;
	}
	public void setBibleTitle(String bibleTitle) {
		this.bibleTitle = bibleTitle;
	}
	public String getBibleSubTitle() {
		return bibleSubTitle;
	}
	public void setBibleSubTitle(String bibleSubTitle) {
		this.bibleSubTitle = bibleSubTitle;
	}
}
